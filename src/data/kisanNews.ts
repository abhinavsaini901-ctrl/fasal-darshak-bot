export function getNewsBySlug(slug: string): NewsItem | undefined {
  return NEWS_POOL.find((n) => n.slug === slug || n.id === slug);
}

export function getRelatedNews(slug: string, limit = 3): NewsItem[] {
  const current = getNewsBySlug(slug);
  if (!current) return [];
  return NEWS_POOL.filter(
    (n) => n.slug !== current.slug && n.category === current.category,
  )
    .concat(NEWS_POOL.filter((n) => n.slug !== current.slug && n.category !== current.category))
    .slice(0, limit);
}

export function formatNewsTimeAgo(minutes: number, refreshTick = 0): string {
  const m = minutes + refreshTick;
  if (m < 60) return `${m} मिनट पहले`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h} घंटे पहले`;
  return `${Math.floor(h / 24)} दिन पहले`;
}
